# ------------------ Main ------------------

def process_results():
    today = datetime.now().strftime("%d-%m")
    yesterday = (datetime.now() - timedelta(days=1)).strftime("%d-%m")
    state = load_state()
    soup = fetch_html()

    # ‡§®‡§Ø‡§æ ‡§¶‡§ø‡§® ‚Üí ‡§ï‡§≤ ‡§ï‡§æ ‡§™‡•Ç‡§∞‡§æ result ‡§≠‡•á‡§ú‡•ã (fallback)
    if state.get("date") != today:
        yres = parse_chart_for_date(soup, yesterday)
        if yres:
            msg = build_message(yesterday, yres)
            send_message(msg)
        state = {"date": today, "sent_results": {}}
        save_state(state)

    # ‡§Ü‡§ú ‡§ï‡•á live ‡§î‡§∞ chart results
    todays_live = parse_live(soup)
    todays_chart = parse_chart_for_date(soup, today)

    # Merge: live > chart
    final_results = {}
    for g in TARGETS:
        if g in todays_live:
            final_results[g] = todays_live[g]
        elif g in todays_chart:
            final_results[g] = todays_chart[g]

    # ‡§Ö‡§™‡§°‡•á‡§ü‡•ç‡§∏ ‡§ö‡•á‡§ï ‡§ï‡§∞‡•ã
    updates = {}
    for g, val in final_results.items():
        if g not in state.get("sent_results", {}) or state["sent_results"][g] != val:
            updates[g] = val

    # ‡§Ö‡§ó‡§∞ ‡§ï‡•Å‡§õ ‡§Ö‡§™‡§°‡•á‡§ü ‡§π‡•à ‚Üí ‡§≠‡•á‡§ú‡•ã
    if updates:
        msg = build_message(today, updates)
        send_message(msg)
        state.setdefault("sent_results", {}).update(updates)
        state["date"] = today
        save_state(state)

def main():
    while True:
        now = datetime.now()
        min_delta = timedelta(days=1)
        for timestr in TIMINGS.values():
            h, m = map(int, timestr.split(':'))
            target = now.replace(hour=h, minute=m, second=0, microsecond=0)
            if target <= now:  # ‡§Ö‡§ó‡§∞ ‡§™‡§æ‡§∏‡•ç‡§ü ‡§π‡•à, ‡§§‡•ã ‡§®‡•á‡§ï‡•ç‡§∏‡•ç‡§ü ‡§°‡•á
                target += timedelta(days=1)
            delta = target - now
            if delta < min_delta:
                min_delta = delta
        sleep_seconds = max(min_delta.total_seconds(), 0)
        print(f"‡§∏‡•ç‡§≤‡•Ä‡§™ ‡§ï‡§∞ ‡§∞‡§π‡§æ ‡§π‡•Ç‡§Å {sleep_seconds} ‡§∏‡•á‡§ï‡§Ç‡§°‡•ç‡§∏ ‡§§‡§ï...")  # ‡§°‡•Ä‡§¨‡§ó ‡§ï‡•á ‡§≤‡§ø‡§è
        time.sleep(sleep_seconds)
        process_results()  # ‡§Ö‡§¨ ‡§™‡•ç‡§∞‡•ã‡§∏‡•á‡§∏ ‡§ï‡§∞‡•ã

if __name__ == "__main__":
    main()